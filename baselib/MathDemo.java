package baselib;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.random());
        System.out.println(Math.round(15.5));
        System.out.println(Math.round(-15.5));
        System.out.println(Math.tan(Math.PI / 2));
        System.out.println(Math.pow(2, 3));
    }
}
